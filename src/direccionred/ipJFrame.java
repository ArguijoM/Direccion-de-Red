    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        // TODO add your handling code here:
        try{
            panelResultado.setEditable(false);
            clase.setEditable(false);
            String ip = direccionIP.getText();
            DireccionRed r1 = new DireccionRed(ip);   
            String[] arreglo = r1.contarOctetos(ip);
            int[] octeto=new int[4];
            int[] octetoBin = new int[4];
            for(int i=0; i< arreglo.length;i++){
                octeto[i] = Integer.parseInt(arreglo[i]);//Convertir a enteros los 4 octetos
                octetoBin[i] = r1.toBinario(octeto[i]);//Convertir a binario los 4 octetos
            }
            int ipint = octeto[0];//Clasificar las redes por clases
            //RESTRICCIONES
            if(ipint==127){//No se puede porque est치 reservada
                JOptionPane.showMessageDialog(panel, "La direccion 127 est치 reservada", "Error", JOptionPane.ERROR_MESSAGE);
                direccionIP.setText("");
                subred.setText("");
                panelResultado.setText("");
            }else if(ipint == 0){
                JOptionPane.showMessageDialog(panel, "La direccion 0 est치 reservada", "Error", JOptionPane.ERROR_MESSAGE);
                direccionIP.setText("");
                subred.setText("");
                panelResultado.setText("");
            }else if(ipint == 255){
                JOptionPane.showMessageDialog(panel, "La direccion 255 est치 reservada", "Error", JOptionPane.ERROR_MESSAGE);
                direccionIP.setText("");
                subred.setText("");
                panelResultado.setText("");
            }else{
                //ASIGNACIONES
                if(ipint<127){
                    r1.setClase("A");
                    if(subred.getText().equals("")){
                        r1.setMascara("255.0.0.0");
                        subred.setText(r1.getMascara());
                    }else
                        r1.setMascara(subred.getText());
                }else if(ipint>127 && ipint<=191){
                    r1.setClase("B");
                    if(subred.getText().equals("")){
                        r1.setMascara("255.255.0.0");
                        subred.setText(r1.getMascara());
                    }else
                      r1.setMascara(subred.getText());
                }else if(ipint>191 && ipint<225){
                    r1.setClase("C");
                    if(subred.getText().equals("")){
                        r1.setMascara("255.255.255.0");
                        subred.setText(r1.getMascara());
                    }
                    else
                        r1.setMascara(subred.getText());
                }
            }
            
            clase.setText(r1.getClase());
            panelResultado.setText("");
            for(int i=0; i< arreglo.length;i++){
                panelResultado.setText(panelResultado.getText()+Integer.parseInt(r1.direcciondeRed()[i],2)+".");
            }
        }catch(Exception e){
              JOptionPane.showMessageDialog(panel, "Ingrese una direccion IP valida", "Error", JOptionPane.ERROR_MESSAGE);  
        }
       
    }//GEN-LAST:event_aceptarActionPerformed

